# A program that calculates the number of characters
# of your name, surname and full name

name = 'Anna'      # replace with your name if needed
surname = 'May'      # replace with your surname if needed

characters_in_name = len(name)          # number of characters in name
characters_in_surname = len(surname)    # number of characters in surname
full_name = name + " " + surname        # full name with space
characters_in_full_name = len(full_name)

print(f'Your name has {characters_in_name} characters')
print(f'Your surname has {characters_in_surname} characters')
print(f'Your full name has {characters_in_full_name} characters')


